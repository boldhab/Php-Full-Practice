<?php
// Arithmetic expressions in PHP
// An arithmetic expression is a combination of numbers, variables, and operators that can be evaluated to produce a value.
// The basic arithmetic operators in PHP are:
// 1. Addition (+): Adds two numbers together.
// 2. Subtraction (-): Subtracts one number from another.
// 3. Multiplication (*): Multiplies two numbers together.
// 4. Division (/): Divides one number by another.
// 5. Modulus (%): Returns the remainder of a division operation.
// 6. Exponentiation (**): Raises a number to the power of another number.

// Simple calculator example
$num1 = 10;
$num2 = 5;
$addition = $num1 + $num2; 
$subtraction = $num1 - $num2; 
$multiplication = $num1 * $num2; 
$division = $num1 / $num2; 
$modulus = $num1 % $num2; 
$exponentiation = $num1 ** $num2; 
echo "Addition: " . $addition . "<br>"; // Output: 15
echo "Subtraction: " . $subtraction . "<br>"; // Output: 5
echo "Multiplication: " . $multiplication . "<br>"; // Output: 50
echo "Division: " . $division . "<br>"; // Output: 2
echo "Modulus: " . $modulus . "<br>"; // Output: 0
echo "Exponentiation: " . $exponentiation . "<br>"; // Output: 100000


?>