<?php
// String operators in PHP
// The concatenation operator (.) is used to join two strings together.
$string1 = "Hello, ";
$string2 = "World!";
echo $string1 . $string2; // Output: Hello, World!

// The concatenation assignment operator (.=) is used to append a string to an existing string.
$string = "Hello, ";
$string .= "World!";
echo $string; // Output: Hello, World!
?>